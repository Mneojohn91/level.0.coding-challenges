def tempature_Fahrenheit(degree_celsius):
    
    #converting degrees celsius to degrees fahrenheit
    cels= (degree_celsius*(9/5))+32
    print(cels, "degrees fahrenheit")
tempature_Fahrenheit(12)

def tempature_celsius(degree_fahr):
    
    #converting degrees fahrenheit to celsius
    cels= (degree_fahr- 32)*5/9
    print(cels, "degrees celsius")
tempature_celsius(95)

